#include <unistd.h>
#include <stdio.h>

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

int main ()
{
	char src[12] = "Hello World";
	char dest[12] = "";

	ft_strcpy(dest, src);
	printf("src is %s\n", src);
	printf("dest is %s\n", dest);
}
